        function combineInputs(event) {
            event.preventDefault(); // Ngăn chặn form được gửi đi
            const inputs = document.querySelectorAll('input[type="text"]');
            let combinedValue = '';
            inputs.forEach(input => {
                combinedValue += input.value;
            });
            console.log(combinedValue);

            // Điều hướng sang trang authentication/verifyCode
            const url = '{{_WEB_ROOT}}authentication/verifyCodeQr';
            const form = document.createElement('form');
            form.method = 'POST';
            form.action = url;

            // Tạo một input ẩn để chứa giá trị combinedValue
            const combinedValueInput = document.createElement('input');
            combinedValueInput.type = 'hidden';
            combinedValueInput.name = 'combinedValue';
            combinedValueInput.value = combinedValue;

            // Thêm input vào form
            form.appendChild(combinedValueInput);

            // Thêm form vào body và gửi yêu cầu
            document.body.appendChild(form);
            form.submit();
        }
    </script>
